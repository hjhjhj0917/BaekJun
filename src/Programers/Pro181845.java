package Programers;

import java.util.Scanner;

public class Pro181845 {

    static String solution(int n) {
        String answer = "";

        answer = String.valueOf(n);

        return answer;
    }

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int n = sc.nextInt();
        System.out.println(solution(n));
    }
}
